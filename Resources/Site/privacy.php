      <h2>Privacy</h2>
		<p>Every 75 launches (250 in versions less than 4.0 and 300 in versions less than 3.0), we automatically collect some numbers and strings concerning your usage. The information is completely unspecific (no names, emails, device names, IPs, locations, dates, etc.) and only about MBS Now.</p>
	  <p>To send yourself your own current data, navigate to 'Data Uploads' from the home tab, and press 'View My Data'.<br></p>
		Here's what we collect:<div class="well well-sm">system name, version number, model, screen dimensions, current button color, forms tapped, offline schedules tapped, lunch menus tapped, contacts copied, launches, version of MBS Now, if you've sent before, your division (MS/US), grade if MS student, if you're receiving notifications from MBS Now, if you have access to database credentials, database logins copied, date of upload, meeting modification notification preferences, number of times you've RSVPed to club events, club meetings viewed, number of times you've sent yourself your own data, views of full schedule webpage from Today schedule image cells</div>
