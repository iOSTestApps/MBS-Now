      <h2>Privacy</h2>
      <p><b>Our analytics have expanded in version 4.0</b>. This document has been updated to reflect those changes.</p>
      <p>First of all, please note that we do <i>not</i> use a third party for any data collection&mdash;neither open-source nor commercial. Our analytics are 100% written by us, and data are always stored exclusively on MBS servers.</p>
      <h3>On iOS</h3>
	  <p>Every <code>50</code> launches<a href="#footer">*</a>, we automatically collect some numbers and strings concerning your usage. The information is generally user-anonymous (no names, emails, IPs, or locations), although not device-anonymous and only about MBS Now.<br><br>The intent of uploads is to make your experience more tailored for real-world use and as efficient as possible. We also get much more actionable data than, say, a feedback message.</p>
	  <div class="panel panel-default">
		  <div class="panel-heading"><b>What we collect</b></div>
			  <div class="panel-body">
				  <p><tt>system name, version, height, width, forms tapped, offline schedules tapped, lunch menu accesses, contacts tapped, launches, app version, sent data before boolean, MS grade, if you receive dress notifications, if you receive general notifications, if you receive A/B notifications, database logins copied, button color, club auto-check preference, club meeting RSVP button taps, text schedule notifications received, club meetings viewed, divison (MS/US), count of self-initiated data exports, full schedule views from Today image cell, reloads of Today data, service postings viewed, time spent in first (organic only) "what's new" screen, always show tomorrow's schedule in Today boolean, lunch menu views from Today, dress-up notification receipt time, always show news articles in Today boolean, use Today as launch screen boolean, device name, timestamp.</tt></p>
			  </div>
	  </div>
	  <p>To send yourself your own current data, navigate to 'Data Uploads' from the home tab, and press 'View My Data'.</p>
	  <h4>Past procedures</h4>
	  <ul>
	  	<li>In versions less than <code>4.0</code>, uploads occur every <code>250</code> launches.
	  		<ul>
		  		<li>As MBS Now versions greater than four only support iOS versions greater than <code>7.0</code>, if you are running iOS <code>5.y</code> or iOS <code>6.x</code>, this case applies to you. <a href="http://htmlpreview.github.io/?https://raw.githubusercontent.com/mbsdev/MBS-Now/master/Resources/Site/Archive/v38.php">View the archived privacy page for this version</a>.</li>
	  		</ul>
	  	<li>In versions less than <code>3.0</code>, uploads occur every <code>300</code> launches.</li>
	  </ul>
	<h3>On the web</h3>
		<p>When you upload a form, your IP address is logged for security purposes. Other than that, we collect no information about you, your computer, or network as you use MBS Now for web.</p>
	<br><p class="text text-danger">You cannot disabled or stop data collection. To learn about how we mine the data, <a href="mailto:team@gdyer.de">email us</a>.</p>
