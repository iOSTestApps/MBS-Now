      <h2>Privacy</h2>
      <p><b>Our analytics have expanded in version 4.0</b>. This document has been updated to reflect those changes.</p>
      <h3>On iOS</h3>
	  <p>Every <code>50</code> launches<sup>see footer</sup>, we automatically collect some numbers and strings concerning your usage. The information is generally user-anonymous (no names, emails, IPs, or locations), although not device-anonymous and only about MBS Now.<br><br>The intent of uploads is to make your experience more tailored for real-world use and as efficient as possible. We also get much more actionable data than, say, a feedback message.</p>
	  <div class="panel panel-default">
		  <div class="panel-heading"><b>What we collect</b></div>
			  <div class="panel-body">
				  <p><tt>system name, version number, model, screen dimensions, current button color, forms tapped, offline schedules tapped, lunch menus tapped, contacts copied, launches, version of MBS Now, number of uploads from your device, your division (MS/US), grade if MS student, device name, if you're receiving notifications from MBS Now, if you have access to database credentials, database logins copied, date of upload, time at which you receive dress-up notifications (if you do at all), meeting modification notification preferences, number of times you've RSVPed to club events, club meetings viewed, number of times you've sent yourself your own data, views of full schedule webpage from Today schedule (graphical) cells, preference for always viewing tomorrow's schedule in Today, service opportunities viewed, viewing time of What's New interface (from organic display only)</tt></p>
			  </div>
	  </div>
	  <p>To send yourself your own current data, navigate to 'Data Uploads' from the home tab, and press 'View My Data'.</p>
	  <h4>Past procedures</h4>
	  <ul>
	  	<li>In versions less than <code>4.0</code>, uploads occur every <code>250</code> launches.
	  		<ul>
		  		<li>As MBS Now versions greater than four only support iOS versions greater than <code>7.0</code>, if you are running iOS <code>5.y</code> or iOS <code>6.x</code>, this case applies to you. <a href="http://htmlpreview.github.io/?https://raw.githubusercontent.com/mbsdev/MBS-Now/master/Resources/Site/Archive/v38.php">View the archived privacy page for this version</a>.</li>
	  		</ul>
	  	<li>In versions less than <code>3.0</code>, uploads occur every <code>300</code> launches.</li>
	  </ul>
	<h3>On the web</h3>
		<p>When you upload a form, your IP address is logged for security purposes. Other than that, we collect no information about you, your computer, or network as you use MBS Now for web.</p>
	<br><p class="text text-danger">You cannot disabled or stop data collection. To learn about how we mine the data, <a href="mailto:team@gdyer.de">email us</a>.</p>
