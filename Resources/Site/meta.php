<h3><a id="about">About us</a></h3>
	MBS Now was made with the idea to make students', parents ', and faculty members' lives easier. Before MBS Now, there was no medium from which those associated with the school could access crucial information. Hopefully you think this has changed now! We 're facilitating Morristown-Beard's mission of having technology in the classroom. As every student is required to have an iPad, an app 's impact can be felt immediately and on a large scale.
	<br><br>
	We work differently than most high-school and college applications. Rather than being controlled by a company that runs a myriad of apps that have all been formatted to the same template, MBS Now is much more dynamic. We listen to your suggestions and add functionality accordingly.
	<br><br>
	Furthermore, MBS Now will always be maintained by students; as soon as a student-developer moves on, the app and its responsibilities will be passed down to underclassmen.
	<br><br>
	<a href="mailto:team@gdyer.de">Let us know</a> if you have any questions, suggestions, or feedback.
	We hope you enjoy the app!</p>

